    private void loadData() {
        File f = new File(path);
        if (f.exists()) {
            list = IOFile.doc(path);
        }
        for (QuanLy i : list) {
            t.addRow(i.toObjects());
        }
        File fph = new File(pathph);
        if (fph.exists()) {
            listph = IOFile.doc(pathph);
        }
        for (PhongHoc i : listph) {
            jComboBox1.addItem(i.getMa() + "");
        }
        File ftb = new File(pathtb);
        if (ftb.exists()) {
            listtb = IOFile.doc(pathtb);
        }
        for (ThietBi i : listtb) {
            jComboBox2.addItem(i.getMa() + "");
        }
    }

    private void showData(int i) {
        jComboBox1.setSelectedItem(jTable1.getValueAt(i, 0) + "");
        jComboBox2.setSelectedItem(jTable1.getValueAt(i, 1) + "");
        jTextField4.setText(jTable1.getValueAt(i, 4) + "");
        jTextField5.setText(jTable1.getValueAt(i, 5) + "");
    }

    private boolean checkTonTai(int maph, int matb) {
        for (QuanLy i : list) {
            if (maph == i.getMaph() && matb == i.getMatb()) {
                return false;
            }
        }
        return true;
    }

    private PhongHoc getPHbyMa(int ma) {
        for (PhongHoc i : listph) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private ThietBi getTBbyMa(int ma) {
        for (ThietBi i : listtb) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

