    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int maph, matb;
        String tenph, tentb, tinhtrang;
        int soluong;
        try {
            maph = Integer.parseInt(jComboBox1.getSelectedItem().toString());
            matb = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            tenph = getPHbyMa(maph).getTen() + "";
            tentb = getTBbyMa(matb).getTen() + "";
            soluong = Integer.parseInt(jTextField4.getText());
            tinhtrang = jTextField5.getText();
            if (checkTonTai(maph, matb)) {
                QuanLy tmp = new QuanLy(maph, matb, tenph, tentb, soluong, tinhtrang);
                list.add(tmp);
                t.addRow(tmp.toObjects());
            } else {
                JOptionPane.showMessageDialog(this, "Da Ton Tai");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiem tra lai so!");
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int ridx = jTable1.getSelectedRow();
        if (ridx < 0 || ridx > jTable1.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon mot dong tren bang");
        } else {
            int maph, matb;
            String tenph, tentb, tinhtrang;
            int soluong;
            try {
                maph = Integer.parseInt(jComboBox1.getSelectedItem().toString());
                matb = Integer.parseInt(jComboBox2.getSelectedItem().toString());
                tenph = getPHbyMa(maph) + "";
                tentb = getTBbyMa(matb) + "";
                soluong = Integer.parseInt(jTextField4.getText());
                tinhtrang = jTextField5.getText();
                if (checkTonTai(maph, matb)) {
                    QuanLy tmp = new QuanLy(maph, matb, tenph, tentb, soluong, tinhtrang);
                    list.set(ridx, tmp);
                    t.removeRow(ridx);
                    t.insertRow(ridx, tmp.toObjects());
                } else {
                    JOptionPane.showMessageDialog(this, "Da Ton Tai");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Kiem tra lai so!");
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

