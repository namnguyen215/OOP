     private boolean checkTonTai(int macn, int masp) {
        for (TinhCong i : list) {
            if (i.getMacn() == macn && i.getMasp() == masp) {
                JOptionPane.showMessageDialog(this, "Da ton tai!");
                return false;
            }
        }
        return true;
    }
    private boolean checkSoSanPham(int macn,int masp){
        int count=0;
        for (TinhCong i : list) {
            if (i.getMacn() == macn ) {
                count++;
            }
        }
        if(count<=4)
            return true;
        else return false;
    }
