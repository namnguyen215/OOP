    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int macn;
        int masp;
        String tencn;
        String tensp;
        int soluong;
        macn = Integer.parseInt(jComboBox1.getSelectedItem().toString());
        masp = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        tencn = getCNbyMa(macn).getTen();
        tensp = getSPbyMa(masp).getTen();
        soluong = Integer.parseInt(jTextField3.getText());
        if (checkTonTai(macn, masp) && checkSoSanPham(macn, masp)) {
            TinhCong tmp = new TinhCong(macn, masp, tencn, tensp, soluong);
            list.add(tmp);
            tm.addRow(tmp.toObjects());
        }


    }//GEN-LAST:event_btThemActionPerformed

