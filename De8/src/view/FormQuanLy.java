    private void loadData() {
        File f = new File(path);
        if (f.exists()) {
            list = IOFile.doc(path);
        }
        for (BangSapXep i : list) {
            tm.addRow(i.toObjects());
        }

        File flh = new File(pathlh);
        if (flh.exists()) {
            listlh = IOFile.doc(pathlh);
        }
        for (Lop i : listlh) {
            jComboBox2.addItem(i.getMa() + "");
        }

        File fph = new File(pathph);
        if (fph.exists()) {
            listph = IOFile.doc(pathph);
        }
        for (PhongHoc i : listph) {
            jComboBox3.addItem(i.getMa() + "");
        }
    }

    private void showContent(int row) {
        //"Ma Lop","Ten Lop","Ma Phong","Ten Phong"
        jComboBox2.setSelectedItem(jTable1.getValueAt(row, 0) + "");
        jComboBox3.setSelectedItem(jTable1.getValueAt(row, 2) + "");
    }

    private Lop getLopByMa(int ma) {
        for (Lop i : listlh) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private PhongHoc getPHByMa(int ma) {
        for (PhongHoc i : listph) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private boolean isTonTai(int mal, int map) {
        for (BangSapXep i : list) {
            if (i.getMal() == mal && i.getMap() == map) {
                JOptionPane.showMessageDialog(this, "Da Ton Tai!");
                return false;
            }

        }
        return true;
    }

    private boolean isXepDuoc(int sosv, int soghe) {
        if (sosv > soghe) {
            JOptionPane.showMessageDialog(this, "So sinh vien phai nho hon hoac bang so ghe!");
            return false;
        }

        return true;
    }

