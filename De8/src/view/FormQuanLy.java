    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int mal, map;
        String tenl, tenp;
        int sosv, soghe;
        mal = Integer.parseInt(jComboBox2.getSelectedItem().toString());
        map = Integer.parseInt(jComboBox3.getSelectedItem().toString());
        tenl = getLopByMa(mal).getTen();
        tenp = getPHByMa(map).getTen();
        sosv = getLopByMa(mal).getSosv();
        soghe = getPHByMa(map).getSoghe();
        if (isTonTai(mal, map) && isXepDuoc(sosv, soghe)) {
            BangSapXep tmp = new BangSapXep(mal, map, tenl, tenp);
            list.add(tmp);
            tm.addRow(tmp.toObjects());
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0 || row > jTable1.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon mot dong!");
        } else {
            int mal, map;
            String tenl, tenp;
            int sosv, soghe;
            mal = Integer.parseInt(jComboBox2.getSelectedItem().toString());
            map = Integer.parseInt(jComboBox3.getSelectedItem().toString());
            tenl = getLopByMa(mal).getTen();
            tenp = getPHByMa(map).getTen();
            sosv = getLopByMa(mal).getSosv();
            soghe = getPHByMa(map).getSoghe();
            if (isTonTai(mal, map) && isXepDuoc(sosv, soghe)) {
                BangSapXep tmp = new BangSapXep(mal, map, tenl, tenp);
                list.remove(row);
                tm.removeRow(row);
                tm.insertRow(row, tmp.toObjects());
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

