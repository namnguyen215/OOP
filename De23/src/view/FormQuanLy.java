    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        //ma,ten,nhom,giamua,giaban
        try {
            NhanVien nv = getnv(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
            MatHang mh = getmh(Integer.parseInt(jComboBox2.getSelectedItem().toString()));
            int soluong = Integer.parseInt(jTextField1.getText());
            if (isTonTai(nv.getMa(), mh.getMa())) {
                QuanLy tmp = new QuanLy(nv, mh, soluong);
                list.add(tmp);
                tm.addRow(tmp.toObjects());
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiem tra lai so!");
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0 || row > jTable1.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon mot dong!");
        } else {

            try {
                NhanVien nv = getnv(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
                MatHang mh = getmh(Integer.parseInt(jComboBox2.getSelectedItem().toString()));
                int soluong = Integer.parseInt(jTextField1.getText());
                if (isTonTai(nv.getMa(), mh.getMa())) {
                    QuanLy tmp = new QuanLy(nv, mh, soluong);
                    list.set(row, tmp);
                    tm.removeRow(row);
                    tm.insertRow(row, tmp.toObjects());
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Kiem tra lai so!");
            }

        }
    }//GEN-LAST:event_btSuaActionPerformed

