    private NhanVien getnv(int ma) {
        for (NhanVien i : listnv) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private MatHang getmh(int ma) {
        for (MatHang i : listmh) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private boolean isTonTai(int manv, int mamh) {
        for (QuanLy i : list) {
            if (i.getNv().getMa() == manv && i.getMh().getMa() == mamh) {
                JOptionPane.showMessageDialog(this, "Da ton tai!");
                return false;
            }
        }
        return true;
    }
    
    private double tinhCong(int manv){
        double tongtien=0;
        for(QuanLy i:list){
            if(i.getNv().getMa()==manv){
                tongtien+=0.02*i.getSoluong()*i.getMh().getGiaban();
            }
        }
        return tongtien;
    }

