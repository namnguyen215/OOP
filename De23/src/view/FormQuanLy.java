    private void jComboBox3ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox3ItemStateChanged
        // TODO add your handling code here:
        if (jComboBox3.getSelectedIndex() == 1) {
            //thep ten nv
            list.sort(new Comparator<QuanLy>() {
                @Override
                public int compare(QuanLy o1, QuanLy o2) {
                    String[] ten1 = o1.getNv().getTen().split("\\s+");
                    String[] ten2 = o2.getNv().getTen().split("\\s+");
                    if(ten1[ten1.length-1].equalsIgnoreCase(ten2[ten2.length-1]))
                        return o1.getNv().getTen().compareToIgnoreCase(o2.getNv().getTen());
                    else
                        return ten1[ten1.length-1].compareToIgnoreCase(ten2[ten2.length-1]);
                }

            });
        }
        
        if (jComboBox3.getSelectedIndex() == 2) {
            //thep ten nv
            list.sort(new Comparator<QuanLy>() {
                @Override
                public int compare(QuanLy o1, QuanLy o2) {
                    String[] ten1 = o1.getMh().getTen().split("\\s+");
                    String[] ten2 = o2.getMh().getTen().split("\\s+");
                    if(ten1[ten1.length-1].equalsIgnoreCase(ten2[ten2.length-1]))
                        return o1.getNv().getTen().compareToIgnoreCase(o2.getNv().getTen());
                    else
                        return ten1[ten1.length-1].compareToIgnoreCase(ten2[ten2.length-1]);
                }

            });
        }
        tm.setRowCount(0);
        for(QuanLy i:list){
            tm.addRow(i.toObjects());
        }
    }//GEN-LAST:event_jComboBox3ItemStateChanged

