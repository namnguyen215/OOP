    private boolean isTonTai(int magv, int mamh) {
        for (QuanLy i : list) {
            if (i.getGv().getMa() == magv && i.getMh().getMa() == mamh) {
                JOptionPane.showMessageDialog(this, "Da ton tai!");
                return false;
            }
        }
        return true;
    }

    private boolean isOK(int magv, int solop) {
        //đăng ký không quá 3 môn học một học kỳ, mỗi môn cho biết số lớp có thể giảng dạy. Tổng số lớp của tất cả các môn không vượt quá 6.
        int tongmon = 0, tonglop = solop;
        for (QuanLy i : list) {
            if (i.getGv().getMa() == magv) {
                tongmon++;
            }
        }
        if (tongmon >= 3) {
            JOptionPane.showMessageDialog(this, "So mon nho hon 3!");
            return false;
        }
        for (QuanLy i : list) {
            if (i.getGv().getMa() == magv) {
                tonglop += i.getSolop();
            }
        }
        if (tonglop > 6) {
            JOptionPane.showMessageDialog(this, "So lop nho hon 6!");
            return false;
        }
        return true;
    }

