    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:

        try {
            GiangVien gv = getgv(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
            MonHoc mh = getmh(Integer.parseInt(jComboBox2.getSelectedItem().toString()));
            int solop = Integer.parseInt(jTextField1.getText());
            String time = java.time.LocalDateTime.now().toString();
            if (isTonTai(gv.getMa(), mh.getMa()) && isOK(gv.getMa(), solop)) {
                QuanLy tmp = new QuanLy(gv, mh, solop, time);
                list.add(tmp);
                tm.addRow(tmp.toObjects());
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiem tra lai so!");
        }

    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0 || row > jTable1.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Hay chon mot dong!");
        } else {
            try {
                GiangVien gv = getgv(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
                MonHoc mh = getmh(Integer.parseInt(jComboBox2.getSelectedItem().toString()));
                int solop = Integer.parseInt(jTextField1.getText());
                String time = java.time.LocalDateTime.now().toString();
                if (isTonTai(gv.getMa(), mh.getMa()) && isOK(gv.getMa(), solop)) {
                    QuanLy tmp = new QuanLy(gv, mh, solop, time);
                    list.set(row, tmp);
                    tm.removeRow(row);
                    tm.insertRow(row, tmp.toObjects());
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Kiem tra lai so!");
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

