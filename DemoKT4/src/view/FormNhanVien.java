    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:
        int ma, sodt;
        String ten, diachi;
        try {
            ma = Integer.parseInt(jTextField1.getText());
            jTextField1.setText(NhanVien.getSma()+"");
            ten = jTextField2.getText();
            diachi = jTextField3.getText();
            if(ten.isEmpty() || diachi.isEmpty()) throw new TrongException();
            sodt = Integer.parseInt(jTextField4.getText()); 
            NhanVien tmp=new NhanVien(ma, ten, diachi, sodt);
            list.add(tmp);
            tm.addRow(tmp.toObject());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiem tra so nhap vao");
            return;
        }catch(TrongException e){
            JOptionPane.showMessageDialog(this, "Kiem tra so nhap vao");
            return;
        }

    }//GEN-LAST:event_btThemActionPerformed

