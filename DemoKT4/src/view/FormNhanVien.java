    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row_idx=jTable1.getSelectedRow();
        int ma, sodt;
        String ten, diachi;
        try {
            ma = Integer.parseInt(jTextField1.getText());
            ten = jTextField2.getText();
            diachi = jTextField3.getText();
            if(ten.isEmpty() || diachi.isEmpty()) throw new TrongException();
            sodt = Integer.parseInt(jTextField4.getText()); 
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiem tra so nhap vao");
            return;
        }catch(TrongException e){
            JOptionPane.showMessageDialog(this, "Kiem tra so nhap vao");
            return;
        }
        NhanVien tmp=new NhanVien(ma, ten, diachi, sodt);
        list.set(row_idx, tmp);
        tm.removeRow(row_idx);
        tm.insertRow(row_idx, tmp.toObject());
    }//GEN-LAST:event_btSuaActionPerformed

