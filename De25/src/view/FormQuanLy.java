    private void loadData() {
        File f = new File(path);
        if (f.exists()) {
            list = IOFile.doc(path);
        }
        for (QuanLy i : list) {
            tm.addRow(i.toObjects());
        }

        File fcn = new File(pathcn);
        if (fcn.exists()) {
            listcn = IOFile.doc(pathcn);
        }
        for (CongNhan i : listcn) {
            jComboBox1.addItem(i.getMa() + "");
        }

        
        for (CongNhan i : listcn) {
            jComboBox4.addItem(i.getMa() + "");
        }
        
        File fx = new File(pathx);
        if (fx.exists()) {
            listx = IOFile.doc(pathx);
        }
        for (Xuong i : listx) {
            jComboBox2.addItem(i.getMa() + "");
        }
    }

