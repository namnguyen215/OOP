    private CongNhan getcn(int ma) {
        for (CongNhan i : listcn) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private Xuong getx(int ma) {
        for (Xuong i : listx) {
            if (i.getMa() == ma) {
                return i;
            }
        }
        return null;
    }

    private boolean isTonTai(int macn, int max) {
        for (QuanLy i : list) {
            if (i.getCn().getMa() == macn && i.getX().getMa() == max) {
                JOptionPane.showMessageDialog(this, "Da Ton Tai");
                return false;
            }
        }
        return true;
    }

    private boolean isOK(int macn, int songay) {
        int tongngay = songay;
        for (QuanLy i : list) {
            if (i.getCn().getMa() == macn) {
                tongngay += i.getSongay();
            }
        }
        if (tongngay > 30) {
            JOptionPane.showMessageDialog(this, "So ngay <= 30");
            return false;
        }
        return true;
    }

    private double tinhLuong(int macn){
//        Thu nhập = 450000*Bậc lương*Hệ số
//công việc * (số ngày làm việc/22).
        double luong=0;
        for(QuanLy i:list){
            if(i.getCn().getMa()==macn){
                luong+=45000*i.getCn().getBac()*i.getX().getHeso()*i.getSongay()/22;
            }
        }
        return luong;
    }
