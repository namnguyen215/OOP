    private void btThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btThemActionPerformed
        // TODO add your handling code here:

        try {
            CongNhan cn = getcn(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
            Xuong x = getx(Integer.parseInt(jComboBox2.getSelectedItem().toString()));
            int songay = Integer.parseInt(jTextField1.getText());
            if (isTonTai(cn.getMa(), x.getMa()) && isOK(cn.getMa(), songay)) {
                QuanLy tmp = new QuanLy(cn, x, songay);
                list.add(tmp);
                tm.addRow(tmp.toObjects());
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Kiem tra lai so ngay!");
        }
    }//GEN-LAST:event_btThemActionPerformed

    private void btSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSuaActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if (row < 0 || row > jTable1.getRowCount()) {
            JOptionPane.showMessageDialog(this, "Chon mot dong!");

        } else {
            try {
                CongNhan cn = getcn(Integer.parseInt(jComboBox1.getSelectedItem().toString()));
                Xuong x = getx(Integer.parseInt(jComboBox2.getSelectedItem().toString()));
                int songay = Integer.parseInt(jTextField1.getText());
                if (isTonTai(cn.getMa(), x.getMa()) && isOK(cn.getMa(), songay)) {
                    QuanLy tmp = new QuanLy(cn, x, songay);
                    list.set(row, tmp);
                    tm.removeRow(row);
                    tm.insertRow(row, tmp.toObjects());
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Kiem tra lai so ngay!");
            }
        }
    }//GEN-LAST:event_btSuaActionPerformed

