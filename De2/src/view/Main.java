    private void loadDataMH() {
        File f = new File(pmh);
        if (f.exists()) {
            listmh = IOFile.doc(pmh);
        }
        for (MonHoc i : listmh) {
            tmmh.addRow(i.toObjects());
        }
    }

    private void loadDataSV() {
        File f = new File(psv);
        if (f.exists()) {
            listsv = IOFile.doc(psv);
        }
        for (SinhVien i : listsv) {
            tmsv.addRow(i.toObjects());
        }
    }

    private void loadDataDK() {
        File f = new File(pdk);
        if (f.exists()) {
            listdk = IOFile.doc(pdk);
        }
        for (BangDangKy i : listdk) {
            tmdk.addRow(i.toObjects());
        }
        for (SinhVien i : listsv) {
            jComboBox2.addItem(i.getMa() + "");
        }
        for (MonHoc i : listmh) {
            jComboBox3.addItem(i.getMa() + "");
        }
    }

    private SinhVien getSVbyMa(String ma) {
        for (SinhVien i : listsv) {
            if ((i.getMa() + "").equals(ma)) {
                return i;
            }
        }
        return null;
    }

    private MonHoc getMHbyMa(String ma) {
        for (MonHoc i : listmh) {
            if ((i.getMa() + "").equals(ma)) {
                return i;
            }
        }
        return null;
    }

