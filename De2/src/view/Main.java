    private boolean checkTonTai(String masv, String mamh) {
        for(BangDangKy i:listdk){
            if(i.getMasv().equals(masv) && i.getMamh().equals(mamh)){
                JOptionPane.showMessageDialog(this, "Da ton tai");
                return false;
            }
                
        }
        return true;
    }
